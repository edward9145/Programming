main(i){
   char c[51],t[50],*b;
   for(;gets(c);puts(b)){
      if(c[1]=='g'){
         t[0]=c[2];
         for(i=1;c[i+2]!='\0';i++)
            t[i]=(c[i+2]-'0')^(c[i+1]-'0')+'0';
         t[i]='\0';
         b=t;
      }
      else{
         b=(c[1]=='b')?(&c[2]):(&c[0]);
         for(i=1;b[i]!='\0';i++)
            b[i]=(b[i]-'0')^(b[i-1]-'0')+'0';
      }
   }
}
